#/bin/bash
############################################################
echo '################################################################################################'
echo '#                                          Grabbing href                                       #'
echo '################################################################################################'

grep "href=" index.html

echo '#################################################################################################'
echo '#                                          Grabbing subdomains                                  #'
echo '#################################################################################################'

grep "href=" index.html | grep "\.megacorpone" | grep -v "www\.megacorpone\.com" | head

echo '#################################################################################################'
echo '#                                          Grabbing subdomains small                            #'
echo '#################################################################################################'     

grep "href=" index.html | grep "\.megacorpone" | grep -v "www\.megacorpone\.com" | awk -F "http://" '{print$2}'


echo '#################################################################################################'
echo '#                                          Clean subdomain scan                                 #'
echo '#################################################################################################'

grep "href=" index.html | grep "\.megacorpone" | grep -v "www\.megacorpone\.com" | awk -F "http://" '{print$2}' | cut -d "/" -f 1

echo '#################################################################################################'
echo '#                                          Using host to get the IP of every subdomain          #'
echo '#################################################################################################'

for url in $(cat list.txt);do host $url;done

echo '#################################################################################################'
echo '#                                          Using host to get only the IP of every subdomain     #'
echo '#################################################################################################'

for url in $(cat list.txt);do host $url;done | grep "has address" | cut -d""-f 4 | sort -u > new_ip.txt 
cat new_ip.txt
